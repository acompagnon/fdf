#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	abs;

	(n < 0) ? write(1, "-", 1) : 1;
	abs = (n < 0) ? -n : n;
	if (abs > 9)
		ft_putnbr(abs / 10);
	ft_putchar((abs % 10) + 48);
}
